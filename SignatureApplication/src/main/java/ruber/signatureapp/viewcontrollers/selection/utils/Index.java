package ruber.signatureapp.viewcontrollers.selection.utils;

public class Index {

    private int start;
    private int end;

    public Index(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public int getStart() {
        return start;
    }

    public int getEnd() {
        return end;
    }
}
